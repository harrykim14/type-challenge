type Permutation<T, U = T> 
    = [U] extends [never] // 두 번째 매개변수가 없다면 빈 배열을 리턴
        ? [] 
        : T extends never // 더이상 순회할 수 없을 때란 Exclude로 배열 내에 아무 요소가 없을 때임
            ? [] 
            : [T, ...Permutation<Exclude<U, T>>] // T를 배열에 위치시키고 T와 U를 제외한 나머지 요소로 재귀

